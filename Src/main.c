void updateTouch(void const * argument){
  GUI_PID_STATE TS_State;
  TS_StateTypeDef ts;

  while(1){
    BSP_TS_GetState(&ts);
    TS_State.Pressed = ts.touchDetected;
    // if(ts.touchDetected){
    //   printf("TOUCHED\r\n");
    // }
    TS_State.x = ts.touchX[0];
    TS_State.y = ts.touchY[0];
    GUI_PID_StoreState(&TS_State);
  }

}

